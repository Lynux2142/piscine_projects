#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int		main(void)
{
	int i;

	i = 0;
	while (i != 26)
	{
		ft_putchar('a' + i);
		ft_putchar('B' + i);
		i += 2;
	}
	ft_putchar('\n');
	return (0);
}
