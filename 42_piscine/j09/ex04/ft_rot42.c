char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if ((str[i] >= 'q' && str[i] <= 'z')
		|| (str[i] >= 'Q' && str[i] <= 'Z'))
			str[i] -= 16;
		else if ((str[i] > 'A' && str[i] <= 'P')
		|| (str[i] > 'A' && str[i] <= 'p'))
			str[i] += 10;
		i++;
	}
	return (str);
}
