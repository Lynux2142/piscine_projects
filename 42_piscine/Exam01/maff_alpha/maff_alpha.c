#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int		main(void)
{
	char i;

	i = 'a';
	while (i != ('z' + 1))
	{
		if ((i % 2) != 0)
			ft_putchar((i - 1) + 1);
		else
			ft_putchar((i - 1) - 31);
		i++;
	}
	ft_putchar('\n');
	return (0);
}
