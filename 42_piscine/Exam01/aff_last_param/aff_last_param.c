#include <stdio.h>

int		main(int argc, char **argv)
{
	int i;

	i = 0;
	(void)argv[1];
	while (argv[i] != '\0')
		i++;
	if (argv[1] == NULL)
		printf("\n");
	else
		printf("%s\n", argv[i - 1]);
	return (0);	
}
