char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	temp;

	i = 0;
	while (str[i] != '\0')
		++i;
	--i;
	j = 0;
	while (i >= 0)
	{
		temp = str[i];
		str[i] = str[j];
		str[j] = temp;
		++j;
		--i;
	}
	str[j] = '\0';
	return (str);
}
